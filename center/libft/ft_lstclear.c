#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*tmp;

	while (*lst)
	{
		tmp = lst[0]->next;
		del(*lst);
		*lst = tmp;
	}
	*lst = NULL;
}
